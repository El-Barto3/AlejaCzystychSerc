    <h5>Informacje w formularzach</h5>
    Serwis zbiera informacje podane dobrowolnie przez użytkownika, może zapisywać ponadto informacje o parametrach połączenia. Administracja serwisu zastrzega sobie prawo do odrzucania i usuwania wydarzeń, postów, oraz profili użytkowników zawierających treści niezgodne z regulaminem i netykietą. </br>    
    Dane w formularzu nie są udostępniane podmiotom trzecim, a jedynie przetwarzane w celu wynikającym z funkcji konkretnego formularza, np w celu stworzenia profilu zmarłej osoby.</br>
    </br>
    <h5>Informacja o plikach cookies</h5>
    Serwis korzysta z plików cookies.
    Pliki cookies wykorzystywane są w następujących celach tworzenia statystyk, które pomagają zrozumieć, w jaki sposób Użytkownicy Serwisu korzystają ze stron internetowych, co umożliwia ulepszanie ich struktury i zawartości, oraz utrzymanie sesji
    Użytkownika Serwisu (po zalogowaniu), dzięki której Użytkownik nie musi na każdej podstronie Serwisu ponownie wpisywać loginu i hasła.
    </br>
    </br>
    <h5>Logi serwera</h5>
    Przeglądane zasoby identyfikowane są poprzez adresy URL. Ponadto zapisowi mogą podlegać informacje o warunkach w jakich wystąpił błąd. Te dane są wykorzystywane jedynie dla celów administrowania serwerem.
    </br>
    </br>
    <h5> Udostępnienie danych</h5>
    Dane podlegają udostępnieniu podmiotom zewnętrznym wyłącznie w granicach prawnie dozwolonych. Informacje umożliwiające identyfikację osoby fizycznej są udostępniane wyłączenie za zgodą tej osoby. Operator może mieć obowiązek udzielania informacji
    zebranych przez Serwis upoważnionym organom na podstawie zgodnych z prawem żądań w zakresie wynikającym z żądania. </br>

    Dane osobowe mogą zbierać i przetwarzać następujący Operatorzy, u których można zażądać zmiany w przetwarzaniu danych osobowych lub ich usunięcia: portal społecznościowy Facebook - zmiany danych osobowych można dokonać w profilu po zalogowaniu na
