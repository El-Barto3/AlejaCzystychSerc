      <p class="px-3 text-center text-break">Jesteśmy uczniami Zespołu Szkół Technicznych i Ogólnokształcących im. Stefana Banacha w Jarosławiu i działając w ramach olimpiady "Zwolnieni z Teorii" pragniemy zwrócić uwagę i zaproponować rozwiązanie problemu jakim jest zanikanie pamięci i historii życia naszych przodków.
        
        Naszym celem jest stworzenie darmowego portalu społecznościowego, dostępnego dla każdego zainteresowanego. Początkowo planujemy objąć zasięgiem lokalną społeczność Jarosławia, jednak nie wykluczamy możliwości rozwinięcia się nawet na całą Polskę!
