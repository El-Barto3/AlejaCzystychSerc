        Przeniesienie cmentarza do internetu uchroni go przed upływem czasu, katastrofami naturalnymi, a także innymi destruktywnymi czynnikami, takimi jak wandalizm. Dodatkowo nasz portal utrwali pamięć o zmarłych i zamieni się w ogromną skarbnicę wiedzy dla przyszłych pokoleń badających swoje dawne korzenie.</p>
